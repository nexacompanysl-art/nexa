function formatEUR(n) {
  return new Intl.NumberFormat('es-ES', { style: 'currency', currency: 'EUR', maximumFractionDigits: 0 }).format(n);
}

function updateROI() {
  var clientsEl = document.getElementById('roi-clients');
  if (!clientsEl) return;
  var clients = parseInt(clientsEl.value) || 0;
  var hours = parseInt(document.getElementById('roi-hours').value) || 0;
  var ticket = parseInt(document.getElementById('roi-ticket').value) || 0;
  var lostLeads = parseInt(document.getElementById('roi-leads').value) || 0;

  document.getElementById('roi-clients-value').textContent = clients;
  document.getElementById('roi-hours-value').textContent = hours;
  document.getElementById('roi-ticket-value').textContent = ticket + ' \u20AC';
  document.getElementById('roi-leads-value').textContent = lostLeads;

  var hourlyRate = 25;
  var operationalLoss = hours * hourlyRate * 4;
  var revenueLoss = lostLeads * ticket;
  var totalLoss = operationalLoss + revenueLoss;
  var annualLoss = totalLoss * 12;

  document.getElementById('roi-operational-loss').textContent = formatEUR(operationalLoss);
  document.getElementById('roi-revenue-loss').textContent = formatEUR(revenueLoss);
  document.getElementById('roi-total-loss').textContent = formatEUR(totalLoss);
  document.getElementById('roi-annual-loss').textContent = formatEUR(annualLoss);
}

var roiSliders = document.querySelectorAll('.roi-slider');
if (roiSliders.length) {
  roiSliders.forEach(function(slider) { slider.addEventListener('input', updateROI); });
  updateROI();
}

// Step card animations
function animateStepCards() {
  var cards = document.querySelectorAll('.step-card');
  if (!cards.length) return;
  var observer = new IntersectionObserver(function(entries) {
    entries.forEach(function(entry) {
      if (!entry.isIntersecting) return;
      var card = entry.target;
      var index = Array.from(cards).indexOf(card);
      setTimeout(function() {
        var metrics = card.querySelectorAll('.step-metric-value');
        if (metrics.length) {
          metrics.forEach(function(el) {
            var text = el.textContent.trim();
            var suffix = text.replace(/[\d.,]+/g, '');
            var target = parseFloat(text) || 0;
            var duration = 1200;
            var start = performance.now();
            function countUp(now) {
              var elapsed = now - start;
              var progress = Math.min(elapsed / duration, 1);
              var eased = 1 - Math.pow(1 - progress, 3);
              var current = eased * target;
              el.textContent = (target % 1 === 0 ? Math.round(current) : current.toFixed(0)) + suffix;
              if (progress < 1) requestAnimationFrame(countUp);
            }
            requestAnimationFrame(countUp);
          });
        }
        var bars = card.querySelectorAll('.step-wf-fill');
        if (bars.length) {
          bars.forEach(function(bar) {
            var w = bar.style.width;
            bar.style.transition = 'none';
            bar.style.width = '0%';
            bar.offsetHeight;
            bar.style.transition = 'width 1.2s cubic-bezier(.16,1,.3,1)';
            bar.style.width = w;
          });
        }
        var intItems = card.querySelectorAll('.step-int-item');
        if (intItems.length) {
          intItems.forEach(function(item, i) {
            setTimeout(function() {
              item.style.opacity = '1';
              item.style.transform = 'translateY(0)';
            }, i * 150);
          });
          setTimeout(function() {
            intItems.forEach(function(item, i) {
              setTimeout(function() {
                var toggle = item.querySelector('.step-toggle');
                if (toggle) toggle.classList.add('step-toggle-on');
              }, i * 200);
            });
          }, intItems.length * 150 + 200);
        }
        var changes = card.querySelectorAll('.step-change-item');
        if (changes.length) {
          changes.forEach(function(item, i) {
            setTimeout(function() {
              item.style.opacity = '1';
              item.style.transform = 'translateX(0)';
              var tag = item.querySelector('.step-change-tag');
              if (tag) {
                tag.style.transform = 'scale(1)';
                tag.style.opacity = '1';
              }
            }, i * 120);
          });
        }
      }, index * 200);
      observer.unobserve(card);
    });
  }, { threshold: 0.2 });
  cards.forEach(function(c) { observer.observe(c); });
}
document.addEventListener('DOMContentLoaded', animateStepCards);

