(function(){
  var prev = document.getElementById('csPrev');
  var next = document.getElementById('csNext');
  var dots = document.querySelectorAll('.cs-dot');
  var bgEl = document.getElementById('csBg');
  var logoEl = document.getElementById('csLogo');
  var textEl = document.getElementById('csText');
  var innerEl = document.getElementById('csOverlayInner');
  if (!prev || !next || !bgEl) return;
  function getSlide(i){
    var lang = document.documentElement.lang || 'es';
    var t = translations[lang] || translations.es;
    var slides = [
      { bg: 'images/cs1.png', logo: t.csLogo1 || 'Desde el departamento de Nexa', text: t.csText1 || '' },
      { bg: 'images/cs2.png', logo: t.csLogo2 || 'Nexa: Agencia de Automatización', text: t.csText2 || '' }
    ];
    return slides[i];
  }
  var current = 0;
  var animating = false;
  function goTo(i){
    if (animating) return;
    if (i < 0) i = 1;
    if (i > 1) i = 0;
    if (i === current) return;
    animating = true;
    var s = getSlide(i);
    innerEl.style.transform = 'translateY(20px)';
    innerEl.style.opacity = '0';
    bgEl.style.opacity = '0';
    setTimeout(function(){
      bgEl.style.backgroundImage = 'url(' + s.bg + ')';
      bgEl.style.opacity = '1';
      logoEl.textContent = s.logo;
      textEl.textContent = s.text;
      innerEl.style.transform = 'translateY(0)';
      innerEl.style.opacity = '1';
      current = i;
      dots.forEach(function(d, idx){ d.classList.toggle('active', idx === current); });
      animating = false;
    }, 400);
  }
  function renderCurrent(){
    var s = getSlide(current);
    logoEl.textContent = s.logo;
    textEl.textContent = s.text;
  }
  prev.addEventListener('click', function(){ goTo(current - 1); });
  next.addEventListener('click', function(){ goTo(current + 1); });
  dots.forEach(function(d, i){ d.addEventListener('click', function(){ goTo(i); }); });
  bgEl.style.backgroundImage = 'url(images/cs1.png)';
  bgEl.style.opacity = '1';
  renderCurrent();
  setTimeout(function(){
    innerEl.style.transform = 'translateY(0)';
    innerEl.style.opacity = '1';
  }, 200);
  // react to language changes
  var origSet = window.setLanguage;
  window.setLanguage = function(lang){
    if (origSet) origSet(lang);
    renderCurrent();
  };
})();

